// Igual ou Diferente  
console.log(5 == "5"); // Aqui olha se o valor é igual
console.log(5 === "5"); // boa prática - Aqui olha se o valor é igual e Se é do mesmo tipo Number-String...

console.log(5 != "5"); // Aqui mesma coisa de cima != diferente
console.log(5 !== "5"); // boa prática - Aqui mesma coisa de cima !== diferente

// Maior e Menor
console.log(10 > 20); // maior
console.log(10 < 20); // menor


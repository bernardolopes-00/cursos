// Operadores Aritméticos avançados
// Exponenciação **
let base = 2;
let expoente = 3;
let resultado = base ** expoente; // 2 seja elevado a 3
console.log(resultado);

// Incremento ++ (adicionar o valor de 1) Decremento -- (diminuir o valor 1)
let controle = 0;
console.log(--controle);
console.log(--controle);
console.log(--controle);
console.log(--controle);
